export function catchallVhost(errorsDir) {
  return `# Generated by @avelor/vhost — do not edit
<VirtualHost *:80>
    ServerName _
    DocumentRoot ${errorsDir}
    <Directory ${errorsDir}>
        Options +MultiViews -Indexes
        Require all granted
    </Directory>
    RewriteEngine On
    RewriteRule ^ /catchall [PT,L]
</VirtualHost>

<VirtualHost *:443>
    ServerName _
    SSLEngine on
    SSLCertificateFile /etc/ssl/certs/ssl-cert-snakeoil.pem
    SSLCertificateKeyFile /etc/ssl/private/ssl-cert-snakeoil.key
    DocumentRoot ${errorsDir}
    <Directory ${errorsDir}>
        Options +MultiViews -Indexes
        Require all granted
    </Directory>
    RewriteEngine On
    RewriteRule ^ /catchall [PT,L]
</VirtualHost>
`
}

export function securityConf() {
  return `# Generated by @avelor/vhost — do not edit
ServerTokens Prod
ServerSignature Off
Header always set Server              "vhost"
Header always set X-Powered-By       "avelor/vhost"
Header always set X-Content-Type-Options "nosniff"
Header always set X-Frame-Options    "SAMEORIGIN"
Header always set Referrer-Policy    "strict-origin-when-cross-origin"
`
}

export function errorsConf(errorsDir, errorCodes) {
  const docs = errorCodes.map(c => `ErrorDocument ${c} /vhost-errors/${c}`).join('\n')
  return `# Generated by @avelor/vhost — do not edit
Alias /vhost-errors ${errorsDir}
<Directory ${errorsDir}>
    Options +MultiViews -Indexes
    AddType application/json .json
    AddType application/xml  .xml
    AddType text/plain       .txt
    Header always set X-Powered-By "avelor/vhost"
    Require all granted
</Directory>

${docs}
`
}
