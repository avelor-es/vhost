export function staticVhost(domain, cfg) {
  const aliases      = cfg.aliases.length ? `    ServerAlias ${cfg.aliases.join(' ')}\n` : ''
  const allNames     = [domain, ...cfg.aliases]
  const rewriteConds = allNames
    .map((n, i) => `    RewriteCond %{SERVER_NAME} =${n}${i < allNames.length - 1 ? ' [OR]' : ''}`)
    .join('\n')
  const sslBlock = cfg.ssl
    ? `    SSLEngine on\n    SSLCertificateFile ${cfg.ssl.cert}\n    SSLCertificateKeyFile ${cfg.ssl.key}`
    : `    # SSL managed by Certbot`

  return `# Generated by @avelor/vhost — do not edit
<VirtualHost *:80>
    ServerName ${domain}
${aliases}    RewriteEngine on
${rewriteConds}
    RewriteRule ^ https://%{SERVER_NAME}%{REQUEST_URI} [END,NE,R=permanent]
</VirtualHost>

<VirtualHost *:443>
    ServerName ${domain}
${aliases}
    DocumentRoot ${cfg.root}
    <Directory ${cfg.root}>
        Options -Indexes +FollowSymLinks
        AllowOverride All
        Require all granted
    </Directory>

    ErrorLog  \${APACHE_LOG_DIR}/${domain}-error.log
    CustomLog \${APACHE_LOG_DIR}/${domain}-access.log combined
${sslBlock}
</VirtualHost>
`
}

